package edu.hm.youtube;

public class ChannelInformation {
	
	private int channelSearchHits;
	
	private String channelID;
	
	private int channelName;

	public int getChannelSearchHits() {
		return channelSearchHits;
	}

	public void setChannelSearchHits(int channelSearchHits) {
		this.channelSearchHits = channelSearchHits;
	}

	public String getChannelID() {
		return channelID;
	}

	public void setChannelID(String channelID) {
		this.channelID = channelID;
	}

	public int getChannelName() {
		return channelName;
	}

	public void setChannelName(int channelName) {
		this.channelName = channelName;
	}
}
